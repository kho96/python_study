# tuple? -- 리스트와 유사하지만, 요소의 값을 변경할 수 없다. []대신 () 사용

t1 = () #데이터가 없는 튜플
print(t1) #()
t1 = (1,) #원소가 1개인 튜플은 요소끝에 ,를 붙인다.
print(t1) 

# 원소가 여러개인 튜플
t1 = (1, 2, 3)
print(t1)

# 소괄호 없이 사용
t1 = 1, 2, 3
print(t1)

# 튜플을 원소로 갖는 튜플
t1 = ("a", "b", ("ab", "cd"))
print(t1)


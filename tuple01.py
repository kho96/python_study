# 튜플 읽어오기(가능)
t1 = (1, 2, 'a', 'b')
print(t1)
print(t1[0])
print(t1[1:4])

# 튜플 더하기
t1 = (1, 2, 3)
t2 = (4, 5, 6, 7)
print(t1+t2) #t1, t2는 변하지 않았기에 리스트와 마찬가지로 합치는 것이 가능함.
print(t1 *3)

# 원소의 갯수
print(len(t1))